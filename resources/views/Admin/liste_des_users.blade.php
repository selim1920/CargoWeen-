const tableBody = document.getElementById("myTable");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

const rowsPerPage = 5; // Number of rows to display per page
let currentPage = 1; // Current page number

// Calculate the total number of pages based on the number of rows and rows per page
const totalPages = Math.ceil(tableBody.rows.length / rowsPerPage);

// Display the initial rows based on the current page
showRows();

// Event listener for "Previous" button
prevBtn.addEventListener("click", () => {
  currentPage--;
  showRows();
});

// Event listener for "Next" button
nextBtn.addEventListener("click", () => {
  currentPage++;
  showRows();
});

function showRows() {
  // Calculate the starting and ending row indices based on the current page and rows per page
  const startIndex = (currentPage - 1) * rowsPerPage;
  const endIndex = startIndex + rowsPerPage;

  // Hide all rows first
  for (let i = 0; i < tableBody.rows.length; i++) {
    tableBody.rows[i].style.display = "none";
  }

  // Show only the rows that should be displayed on the current page
  for (let i = startIndex; i < endIndex && i < tableBody.rows.length; i++) {
    tableBody.rows[i].style.display = "";
  }

  // Disable the "Previous" button if we're on the first page
  prevBtn.disabled = (currentPage === 1);

  // Disable the "Next" button if we're on the last page
  nextBtn.disabled = (currentPage === totalPages);
}

