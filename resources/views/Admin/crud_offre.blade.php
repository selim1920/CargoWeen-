                        const searchBar = document.getElementById("searchBar");
                        searchBar.addEventListener("keyup", function() {
                            const input = searchBar.value.toLowerCase();
                            const table = document.getElementById("myTable");
                            const rows = table.getElementsByTagName("tr");
                            for (let i = 1; i < rows.length; i++) {
                                const num_vol = rows[i].getElementsByTagName("td")[0].textContent.toLowerCase();
                                const nom_compagnie = rows[i].getElementsByTagName("td")[1].textContent.toLowerCase();
                                const airport_depart = rows[i].getElementsByTagName("td")[2].textContent.toLowerCase();
                                const airport_arrive = rows[i].getElementsByTagName("td")[3].textContent.toLowerCase();
                                const jour_depart = rows[i].getElementsByTagName("td")[4].textContent.toLowerCase();
                                const jour_arrive = rows[i].getElementsByTagName("td")[5].textContent.toLowerCase();
                                const tarif_kg = rows[i].getElementsByTagName("td")[6].textContent.toLowerCase();

                                if (num_vol.includes(input) || nom_compagnie.includes(input) || airport_depart.includes(input) || airport_arrive.includes(input) || jour_depart.includes(input) || jour_arrive.includes(input) || tarif_kg.includes(input)) {
                                    rows[i].style.display = "";
                                } else {
                                    rows[i].style.display = "none";
                                }
                            }
                        });
